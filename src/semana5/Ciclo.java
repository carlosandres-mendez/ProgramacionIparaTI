public class Ciclo {
    public static void main(String[] args) {
        
        int j=0;
        
        while(j<1000){
        
            System.out.println(1);
            
            //j = j + 1;
            j++;
        }
        
        
        for(int i=0;i<1000;i++){
            System.out.println(1);
        }

        //i = i + 1 es equivalente a i++
    }
}
